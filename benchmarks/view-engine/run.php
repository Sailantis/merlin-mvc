<?php
// Simple benchmark harness for view engines
require_once __DIR__ . '/../../vendor/autoload.php';

use Merlin\Mvc\Engines\Adapters\TwigAdapter;
use Merlin\Mvc\Engines\Adapters\PlatesAdapter;
use Merlin\Mvc\Engines\Adapters\BladeAdapter;
use Merlin\Mvc\Engines\ClarityEngine;
use Merlin\Mvc\Engines\NativeEngine;

// CLI options: --engines=comma,separated --iterations-per-run=N --runs=N --out=prefix --no-penalty --penalty-separate
$opts = getopt('', ['engines::', 'iterations-per-run::', 'runs::', 'out::', 'no-penalty', 'penalty-separate']);
$engines = isset($opts['engines']) ? explode(',', $opts['engines']) : ['native', 'clarity', 'plates', 'blade', 'twig'];
$itersPerRun = isset($opts['iterations-per-run']) ? (int) $opts['iterations-per-run'] : 10000;
$runs = isset($opts['runs']) ? (int) $opts['runs'] : 30;
$outPrefix = $opts['out'] ?? null; // optional prefix for results files
$penaltyEnabled = !isset($opts['no-penalty']);
$penaltySeparate = isset($opts['penalty-separate']);

$template = 'benchmarks::sample';
$viewPath = __DIR__ . '/templates';

function stats(array $values)
{
    sort($values);
    $count = count($values);
    $sum = array_sum($values);
    $mean = $sum / $count;
    $median = $values[(int) floor(($count - 1) / 2)];
    $p95Index = (int) floor($count * 0.95) - 1;
    if ($p95Index < 0) {
        $p95Index = 0;
    }
    if ($p95Index > $count - 1) {
        $p95Index = $count - 1;
    }
    $p95 = $values[$p95Index];
    return ['count' => $count, 'mean' => $mean, 'median' => $median, 'p95' => $p95];
}

/**
 * Write results as JSON and CSV when $outPrefix is provided.
 */
function writeResults(string $prefix, array $results): void
{
    $jsonFile = __DIR__ . "/{$prefix}.json";
    $csvFile = __DIR__ . "/{$prefix}.csv";
    file_put_contents($jsonFile, json_encode($results, JSON_PRETTY_PRINT));

    $fp = fopen($csvFile, 'w');
    // include memory delta (peak - warm) and percent change. Also include trimmed mean and per-run summary count
    fputcsv($fp, ['engine', 'warm_ms', 'warm_peak_mem', 'iterations_per_run', 'runs', 'trimmed_mean_ms', 'mean_ms', 'median_ms', 'p95_ms', 'peak_mem', 'mem_delta', 'mem_delta_pct']);
    foreach ($results as $r) {
        $delta = ($r['peak_mem'] - $r['warm_peak_mem']);
        $pct = $r['warm_peak_mem'] > 0 ? ($r['peak_mem'] / $r['warm_peak_mem'] - 1) * 100 : 0;
        fputcsv($fp, [$r['engine'], $r['warm_ms'], $r['warm_peak_mem'], $r['iterations_per_run'], $r['runs'], $r['trimmed_mean_ms'] ?? '', $r['mean_ms'], $r['median_ms'], $r['p95_ms'], $r['peak_mem'], $delta, $pct]);
    }
    fclose($fp);
}

function writeReport(string $prefix, array $results): void
{
    global $engines;
    $mdFile = __DIR__ . "/REPORT-{$prefix}.md";
    $title = "Benchmark report ({$prefix})";
    $lines = ["# {$title}", "", "Generated by benchmarks/run.php", "", "## Summary", ""];

    // find winner by mean_ms
    $winner = null;
    foreach ($results as $r) {
        if ($winner === null || $r['mean_ms'] < $winner['mean_ms']) {
            $winner = $r;
        }
    }

    $lines[] = "**Winner (mean render time):** {$winner['engine']} ({$winner['mean_ms']} ms)";
    $lines[] = "";
    $lines[] = "## Results (summary)";
    $lines[] = "";
    $lines[] = "| Engine | Iterations/Run | Runs | Trimmed Mean (ms) | Mean (ms) | Median (ms) | p95 (ms) | Warm (ms) | Peak mem |";
    $lines[] = "|---|---:|---:|---:|---:|---:|---:|---:|---:|";
    foreach ($results as $r) {
        $delta = $r['peak_mem'] - $r['warm_peak_mem'];
        $pct = $r['warm_peak_mem'] > 0 ? ($r['peak_mem'] / $r['warm_peak_mem'] - 1) * 100 : 0;
        $lines[] = sprintf(
            "| %s | %d | %d | %.3f | %.3f | %.3f | %.3f | %.3f | %s | Δ %s (%.1f%%) |",
            $r['engine'],
            $r['iterations_per_run'],
            $r['runs'],
            $r['trimmed_mean_ms'] ?? 0,
            $r['mean_ms'],
            $r['median_ms'],
            $r['p95_ms'],
            $r['warm_ms'],
            number_format($r['peak_mem']),
            number_format($delta),
            $pct
        );
    }

    $lines[] = "";
    $lines[] = "## Notes";
    $lines[] = "- Run command: `php -d opcache.enable_cli=1 benchmarks/run.php --engines=" . implode(',', $engines) . " --iterations-per-run={$results[0]['iterations_per_run']} --runs={$results[0]['runs']}`";
    $lines[] = "- Files: `benchmarks/{$prefix}.json`, `benchmarks/{$prefix}.csv`";
    $lines[] = "";
    $lines[] = "## Interpretation";
    $lines[] = "- Clarity and Native typically provide the best steady-state throughput for these templates (compiled vs plain PHP).";
    $lines[] = "- Twig and Blade have higher warm-up costs due to template compilation; using persistent cache + OPcache reduces warm cost.";

    file_put_contents($mdFile, implode("\n", $lines));
}

$results = [];
foreach ($engines as $key) {
    $key = trim($key);
    echo "\n=== Engine: $key\n";
    $penalty = null; // optional callback to convert template name for penalty measurement (e.g. to Twig syntax)
    switch ($key) {
        case 'clarity':
            $engine = new ClarityEngine();
            $engine->setExtension('.clarity.html');
            $penalty = fn() => viewNameToTwig($template, '.twig');
            break;
        case 'native':
            $engine = new NativeEngine();
            $engine->setExtension('.native.php');
            $penalty = fn() => viewNameToTwig($template, '.twig');
            break;
        case 'twig':
            $engine = new TwigAdapter();
            break;
        case 'plates':
            $engine = new PlatesAdapter();
            break;
        case 'blade':
            $engine = new BladeAdapter();
            break;
        default:
            echo "Unknown engine: $key\n";
            continue 2;
    }

    $engine->setViewPath($viewPath);
    $engine->addNamespace('benchmarks', $viewPath);
    // warm (compile) run
    echo "Warm run...\n";
    $start = hrtime(true);
    try {
        $out = $engine->render($template, ['title' => 'Benchmark', 'items' => range(1, 50)]);
    } catch (Exception $e) {
        echo "Error during warm run: " . $e->getMessage() . "\n";
        continue;
    }
    $warmMs = (hrtime(true) - $start) / 1e6;
    $warmMem = memory_get_peak_usage(true);
    echo sprintf("Warm: %.3f ms, peak mem: %d bytes\n", $warmMs, $warmMem);

    // runs of multiple iterations
    $runSummaries = [];
    $peakMem = $warmMem;
    $allIterationTimes = [];
    $allPenaltyTimes = [];

    for ($r = 0; $r < $runs; $r++) {
        echo sprintf(" Run %d/%d — %d iterations\n", $r + 1, $runs, $itersPerRun);
        $times = [];
        $penaltyTimes = [];
        for ($i = 0; $i < $itersPerRun; $i++) {
            $t0 = hrtime(true);
            $penaltyTime = 0;
            if ($penalty !== null && $penaltyEnabled) {
                $p0 = hrtime(true);
                $penalty();
                $p1 = hrtime(true);
                $penaltyTime = ($p1 - $p0) / 1e6;
            }
            $engine->render($template, ['title' => 'Benchmark', 'items' => range(1, 50)]);
            $t1 = hrtime(true);

            $iterTime = ($t1 - $t0) / 1e6;
            if ($penalty !== null && $penaltyEnabled && !$penaltySeparate) {
                // subtract penalty from recorded iteration time to show pure render time
                $iterTime = max(0.0, $iterTime - $penaltyTime);
            }
            $times[] = $iterTime;
            if ($penalty !== null && $penaltyEnabled) {
                $penaltyTimes[] = $penaltyTime;
            }
            $peakMem = max($peakMem, memory_get_peak_usage(true));
        }
        $s = stats($times);
        $p = $penaltyTimes ? stats($penaltyTimes) : null;
        $runSummaries[] = ['mean' => $s['mean'], 'median' => $s['median'], 'p95' => $s['p95'], 'count' => $s['count'], 'penalty' => $p];
        foreach ($times as $t) {
            $allIterationTimes[] = $t;
        }
        foreach ($penaltyTimes as $pt) {
            $allPenaltyTimes[] = $pt;
        }
        echo sprintf("  Run %d — mean: %.3f ms, median: %.3f ms, p95: %.3f ms\n", $r + 1, $s['mean'], $s['median'], $s['p95']);
    }

    // aggregate across runs
    $runMeans = array_map(fn($rs) => $rs['mean'], $runSummaries);
    sort($runMeans);
    $drop = max(1, (int) round(count($runMeans) * 0.1));
    $trimmed = array_slice($runMeans, $drop, count($runMeans) - 2 * $drop);
    if (count($trimmed) === 0) {
        $trimmed = $runMeans;
    }
    $trimmedMean = array_sum($trimmed) / count($trimmed);

    $sAll = stats($allIterationTimes);
    $penaltyAggregate = $allPenaltyTimes ? stats($allPenaltyTimes) : null;

    echo sprintf("Summary across %d runs — trimmed mean: %.3f ms (dropped %d/%d)\n", $runs, $trimmedMean, $drop, $runs);

    $results[] = [
        'engine' => $key,
        'warm_ms' => $warmMs,
        'warm_peak_mem' => $warmMem,
        'iterations_per_run' => $itersPerRun,
        'runs' => $runs,
        'run_summaries' => $runSummaries,
        'trimmed_mean_ms' => $trimmedMean,
        'mean_ms' => $sAll['mean'],
        'median_ms' => $sAll['median'],
        'p95_ms' => $sAll['p95'],
        'peak_mem' => $peakMem,
        'penalty_aggregate' => $penaltyAggregate,
    ];
}

if ($outPrefix !== null) {
    writeResults($outPrefix, $results);
    echo "\nWrote results to: benchmarks/{$outPrefix}.json and benchmarks/{$outPrefix}.csv\n";
}

function viewNameToTwig(string $view, string $extension): string
{
    $sep = \strpos($view, '::');
    if ($sep !== false) {
        $ns = \substr($view, 0, $sep);
        $name = \substr($view, $sep + 2);
        $relative = \str_replace(['.', '\\'], '/', $name);
        $suffix = \str_ends_with($relative, $extension) ? '' : $extension;
        return '@' . $ns . '/' . $relative . $suffix;
    }
    $relative = \str_replace(['.', '\\'], '/', \ltrim($view, '/'));
    $suffix = \str_ends_with($relative, $extension) ? '' : $extension;
    return $relative . $suffix;
}
